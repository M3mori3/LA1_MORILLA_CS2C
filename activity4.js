//Comparison or Relational Operator

//variables
var firstFavNumb = 3;
var secondFavNumb = 14;

//operates greater than 
 console.log(firstFavNumb > secondFavNumb);

 //operates less than
 console.log(firstFavNumb < secondFavNumb);

//operates greater than or equal
 console.log(firstFavNumb >= secondFavNumb);

//operates less than or equal
 console.log(firstFavNumb <= secondFavNumb);

//operates strict equality
 console.log(firstFavNumb === secondFavNumb);

//operates loose equality
 console.log(firstFavNumb == secondFavNumb);

//operates strict inequality
 console.log(firstFavNumb !== secondFavNumb);

//operates loose inequality
 console.log(firstFavNumb != secondFavNumb);